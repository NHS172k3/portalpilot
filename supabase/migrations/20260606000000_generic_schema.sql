create trigger set_form_fields_updated_at
before update on public.form_fields
for each row execute function public.set_updated_at();

drop trigger if exists set_filing_tasks_updated_at on public.filing_tasks;
create trigger set_filing_tasks_updated_at
before update on public.filing_tasks
for each row execute function public.set_updated_at();

drop trigger if exists set_recommendations_updated_at on public.recommendations;
create trigger set_recommendations_updated_at
before update on public.recommendations
for each row execute function public.set_updated_at();

drop trigger if exists set_field_records_updated_at on public.field_records;
create trigger set_field_records_updated_at
before update on public.field_records
for each row execute function public.set_updated_at();
